Functions 

1. تعريف الدوال (Defining Functions):
تُعرف الدوال باستخدام الكلمة المفتاحية function متبوعة باسم الدالة وقوسين يحتويان على المعاملات (إن وجدت).   
2. استدعاء الدوال (Calling Functions):
يمكن استدعاء الدوال ببساطة عن طريق كتابة اسم الدالة متبوعًا بأقوسين يحتويان على القيم التي سيتم تمريرها كمعاملات (إن وجدت).
3. المعاملات والقيم الافتراضية (Parameters and Default Values):
يمكن للدوال قبول معاملات لتلقي مدخلات عند الاستدعاء. يمكن أيضًا تعيين قيم افتراضية للمعاملات في حالة عدم تمرير قيمة.
4. القيم المرجعة (Return Values):
يمكن للدوال إرجاع قيمة باستخدام الكلمة المفتاحية return. يمكن استخدام القيمة المرجعة في تعبيرات أخرى أو تخزينها في متغيرات.
// مثال عملي:


// تعريف دالة بسيطة تجمع رقمين
function addNumbers($num1, $num2) {
    return $num1 + $num2; 
}
// استدعاء الدالة وتمرير القيم
$sum = addNumbers(10, 20);
echo "<p>The sum of 10 and 20 is: <strong>" . $sum. "</strong></p>";
// تعريف دالة مع قيمة افتراضية
function greetUser($name = "Guest") {
    return "Hello, " . $name . "!";
}
// استدعاء الدالة بدون تمرير قيمة
$greeting1 = greetUser();
echo "<p>" . $greeting1 . "</p>";   
// استدعاء الدالة مع تمرير قيمة
$greeting2 = greetUser("Ali");
echo "<p>" . $greeting2 . "</p>";
