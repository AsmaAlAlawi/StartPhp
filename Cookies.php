Cookies

ملفات تعريف الارتباط (Cookies) هي ملفات نصية صغيرة يرسلها الخادم إلى متصفح الويب،
 ويقوم المتصفح بتخزينها وإرسالها مرة أخرى مع كل طلب لاحق لنفس الموقع.
  الهدف منها هو تذكير الموقع بحالة المستخدم أو تفضيلاته 
 (مثل تذكر اسم المستخدم أو خيار "ابقَ متصلاً").

 setcookie(): الدالة المستخدمة لإنشاء أو تعديل أو حذف ملفات الكوكيز.
$_COOKIE: المصفوفة الشاملة الفائقة التي تحتوي على جميع الكوكيز التي أرسلها المتصفح إلى السيرفر في الطلب الحالي.


مثال عملي
setcookie("user", "Ahmed", time() + 3600);
echo "<p>Cookie 'user' has been set. It will expire in 1 hour.</p>";
if(isset($_COOKIE["user"])) {
    echo "<p>Welcome back, " . $_COOKIE["user"] . "!</p>";
} else {
    echo "<p>Hello, new visitor!</p>";
}
