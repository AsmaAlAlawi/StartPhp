1. تعريف أنواع البيانات الأساسية (PHP Data Types):

في PHP، هناك عدة أنواع بيانات أساسية تشمل:
- السلاسل النصية (Strings): تمثل نصوصًا محاطة بعلامات اقتباس مفردة أو مزدوجة.
$user_name = "Ahmed Salah"; 

- الأعداد الصحيحة (Integers): تمثل الأعداد الصحيحة بدون فاصلة عشرية.
$age = 30;

- الأعداد العشرية (Floats): تمثل الأعداد التي تحتوي على فاصلة عشرية.
$height_in_meters = 1.75;

- القيم البوليانية (Booleans): تمثل القيم الحقيقية (true) أو الخاطئة (false).
$is_student = true; // يمكن أن تكون true أو false

- المصفوفات (Arrays): تمثل مجموعات من القيم المرتبة أو غير المرتبة.
$courses = array("PHP", "JavaScript", "HTML");

- الكائنات (Objects): تمثل مثيلات من الفئات (Classes) التي تحتوي على خصائص وأساليب.

- القيم الفارغة (NULL): تمثل متغيرًا بدون قيمة.     
$future_variable = NULL;

المصدر (Resource): نوع خاص يشير إلى مرجع خارجي، مثل مؤشر لملف مفتوح أو اتصال بقاعدة بيانات.


